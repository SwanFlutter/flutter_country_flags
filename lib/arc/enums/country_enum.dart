  /// ISO country code (2-letter)
  final String code;

  /// Country name (lowercase with dashes)
  final String name;

  /// Get display name (formatted for UI)
  String get displayName {
    return name
        .split('-')
        .map((word) => word[0].toUpperCase() + word.substring(1))
        .join(' ');
  }

  /// Find country by code
  static Country? fromCode(String code) {
    try {
      return Country.values.firstWhere(
        (country) => country.code.toLowerCase() == code.toLowerCase(),
      );
    } catch (e) {
      return null;
    }
  }

  /// Find country by name
  static Country? fromName(String name) {
    final cleanName = name.toLowerCase().trim();
    try {
      return Country.values.firstWhere(
        (country) =>
            country.name == cleanName ||
            country.name.replaceAll('-', ' ') == cleanName ||
            country.displayName.toLowerCase() == cleanName,
      );
    } catch (e) {
      return null;
    }
  }

  /// Get all country codes
  static List<String> get allCodes =>
      Country.values.map((c) => c.code).toList();

  /// Get all country names
  static List<String> get allNames =>
      Country.values.map((c) => c.name).toList();

  /// Get all display names
  static List<String> get allDisplayNames =>
      Country.values.map((c) => c.displayName).toList();
